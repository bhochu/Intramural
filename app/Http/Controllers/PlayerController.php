     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
      $this->validate($request, ['student_id' => 'required']);
      $players = new Player;
      $players->student_id = $request->get('student_id');
      $players->first_name = $request->get('first_name');
      $players->last_name = $request->get('last_name');
      $players->major = $request->get('major');
      $players->team_id = $request->get('team_id');

      $players->save();
      return redirect()->route('player.index')->withStatus(__('Player successfully added.'));
    }

