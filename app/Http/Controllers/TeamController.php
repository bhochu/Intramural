     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
      $this->validate($request, ['name' => 'required']);
      $teams = new Team;
      $teams->sport_id = $request->get('sport_id');
      $teams->name = $request->get('name');
      //$sports = new Sport(['name' => $request->get('name')]);
      $teams->save();
      return redirect()->route('team.index')->withStatus(__('Team successfully added.'));
    }

